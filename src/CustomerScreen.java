    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JTextField screeningNumber = new JTextField();
        
        Object[] message = {
            "Screening Number:", screeningNumber
            
        };
        int option = JOptionPane.showConfirmDialog(null, message, "Enter Screening Number", JOptionPane.OK_CANCEL_OPTION);
        if (option == JOptionPane.OK_OPTION) {
            int check = 6;
            for(int i = 0; i < movie.length ; i++){
                if (movie[i][0].equals(screeningNumber.getText())) {
                    ticket = new BookTicket(control, (String) movies.get(check), screeningNumber.getText());
                    ticket.setVisible(true);
                    this.setVisible(false);
                    condition = true;
                    
                    break;
                }
                check+=7;
            }
            if (!condition) {
                
                condition = false;
            }
        } 
        else {
        JOptionPane.showMessageDialog(null,"No Screening Number Selected");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

