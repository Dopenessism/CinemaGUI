    public CustomerScreen(DataControl control) {
        initComponents();
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
	Date date = new Date();
        jLabel1.setText("Shows Starting In Atleast 30 Minutes. Current Time: "+dateFormat.format(date));
        this.control = control;
        movies = control.getmovies();
        movie = new String[movies.size()/7][6];
        int screnNo = 0;
        int movieNam = 1;
        int cineNam = 2;
        int dateT = 3;
        int timeT = 4;
        int prisT = 5;
        for(int i = 0; i < movie.length; i++ ){
            movie[i][0] = (String) movies.get(screnNo);
            movie[i][1] = (String) movies.get(movieNam);
            movie[i][2] = (String) movies.get(cineNam);
            movie[i][3] = (String) movies.get(dateT);
            movie[i][4] = (String) movies.get(timeT);
            movie[i][5] = (String) movies.get(prisT);
            screnNo = screnNo + 7;
            movieNam = movieNam + 7;
            cineNam = cineNam + 7;
            dateT = dateT + 7;
            timeT = timeT + 7;
            prisT = prisT + 7;
        }
        jTable2.setModel(new javax.swing.table.DefaultTableModel(
            movie,
            new String [] {
                "Screening No.", "Movie Name", "Cinema Name", "Date", "Time", "Price"
            }
        ));
    }

